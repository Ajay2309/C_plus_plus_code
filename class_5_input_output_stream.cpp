#include<iostream>
using namespace std;
int main(){
    int num1, num2 ;
    cout<<"Enter the value of num1 : ";     // '<<' is called insertion operator.
    cin>>num1;      // '>>' is called extraction operator.
   
    cout<<"\nEnter the value of num2 : ";     // '<<' is called insertion operator.
    cin>>num2;      // '>>' is called extraction operator.

    cout<<"\nThe sum is "<<num1 + num2 ;

    return 0;
}

